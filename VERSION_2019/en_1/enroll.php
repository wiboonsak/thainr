              <p align="left">&nbsp;</p>
                <p align="left"><strong>Art.   1.</strong> This Association shall be known as <strong>"SAMAKOM YANGPARATHAI"</strong> in Thai or <strong>"   Tai Kwoa Sieng Joew Jung Huay"</strong> in Chinese,  written in Chinese thus <strong>&quot;泰国橡胶协会&quot;</strong> or<strong> "THE THAI RUBBER ASSOCIATION" </strong>in   English, written in English thus<strong> "THETHAI RUBBER ASSOCIATION"</strong>, and will be   referred to, henceforth, as the Association.</p>
              <p><strong>Art. 2.</strong> In   this rule </p>
              <p class="L35">"the   Association" means the Thai Rubber Association.</p>
              <p class="L35">"the   members" mean the Thai Rubber Association members for all categories. </p>
              <p class="L35">"Board of   directors" means the Board of directors of the Thai Rubber Association. </p>
              <p class="L35">"directors"   mean the directors of the Thai Rubber Association, including President,   Vice-President and Secretary General. </p>
              <p class="L35">"President"   means the President of the Thai Rubber Association. </p>
              <p class="L35">"Vice-President" means the Vice-President of the Thai Rubber   Association. </p>
              <p class="L35">"Secretary   General" means the Secretary General of the Thai Rubber Association.</p>
              <p class="L35">"Manager"   means the manager of the Thai Rubber Association. </p>
              <p class="L35">"Treasurer"   means the Treasurer of the Thai Rubber Association. </p>
              <p class="L35">"Executive   Secretary " means the Executive Secretary of the Thai Rubber Association. </p>
              <p><strong>Art. 3. </strong>The   office of the Association is situated at 45-47 Chotivithayakun 3 Road, Amphur   Hatyai, Songkhla Province 90110. </p>
              <p><strong>Art. 4.</strong> <strong><img src="../th/images/logo.gif" width="132" height="132" hspace="5"></strong>The   symbol of the association is "Lord Indra's three-headed elephant" </p>
              <p align="left">&nbsp;</p>
              </blockquote>
              <blockquote>
                <p><strong>Art. 5.</strong> The   objectives of the Association are: </p>
                <p class="L35">(1) to   promote the trading of natural rubber; </p>
                <p class="L35">(2) to   encourage and help members to solve problems and overcome obstacles, this   includes to enter into negotiation with persons outside the Association, for   mutual benefit in the trading of natural rubber; and to conduct intelligence   study on the natural rubber markets, both domestic and foreign, for the benefit   of the natural rubber trade; </p>
                <p class="L35">(3) to   conduct research on the natural rubber trading and to exchange and disseminate   technical information and knowledge on the natural rubber trade; </p>
                <p class="L35">(4) to   collect statistical or documental or other informations concerning rubber trade   from all members but with the consent of members concerned; </p>
                <p class="L35">(5) to   promote the standardization of rubber qualities; to research and improve on the   processes of smoking, grading, powdering, packing as well as marketing; </p>
                <p class="L35">(6) to   co-operate with the Government in the promotion of natural rubber trade; </p>
                <p class="L35">(7) to seek   agreement or to establish rules of conduct for members to observe to ensure   efficient conducting of the natural rubber trade; </p>
                <p class="L35">(8) to   arbitrate in any conflict between members themselves or between members and   person, or persons outside the association in matters related to the natural   rubber trade; </p>
                <p class="L35">(9) to   abstain from political activities; </p>
                <p class="L35">(10) to own   an immovable property by buying or receiving with fascination or being donated   in order to run all activities of the Association; </p>
                <p class="L35">(11) to   publish a monthly news distribute to members and others. </p>
                <p><strong>Art. 6. </strong>The   manager will run all the Association activities follow the Association policy. </p>
