              <p align="left"><strong>Art.   29. </strong>The General Meetings of the Association are ordinary annual general   meeting and extraordinary general meeting. </p>
              <p><strong>Art. 30. </strong>The   ordinary annual general meeting of the Association shall be convened within 120   days from the end of accounting period according to Art. 2.Chapter 9 and the   Secretary General shall notify all members as to the date, time and place of the   meeting not less than 7 days in advance. </p>
              <p><strong>Art. 31. </strong>The   Board shall be empowered to call Extraordinary general meeting at any time by   notifying the members as to the date, time and place of the meeting, and giving   the meeting agenda not less than 7 days in advance, except in emergency case   when the President or the Vice President can call Extraordinary general meeting   by notifying members through a notice despatched to all members one working day   in advance. The letter of the President or the Vice President recognizing the   emergency nature of the meeting shall be deemed sufficient evidence. </p>
              <p><strong>Art. 32.</strong> Ordinary members numbering not less than two fifths of all ordinary members,   shall have the right to enter thier names in the letter, which shall be handed   to the Board, applying for convening the Extraordinary general meeting, by   specifying the purpose for the calling of such meeting. Upon the Board's   acceptance of such letter, the Secretary General shall send out notice to all   members calling the Extraordinary meeting, fixing there in the date, time and   place of the meeting together with giving the meeting agenda, not less than 7   days in advance.</p>
              <p><strong>Art. 33.</strong> In   the general meeting, the President, or in his absence, the Vice President shall   act as chairman. If both are absent, the meeting shall elect any other director   of the Board, or any ordinary member, or representative of the latter, to take   the chair for that particular meeting. </p>
              <p><strong>Art. 34. </strong>In   any general meeting, ordinary or extraordinary, a quorum shall consist of not   less than one thirds of the total number of ordinary members. Except otherwise   stated in these Rules, in any general meeting the voting shall be decided by a   simple majority. In the event of a tie, the chairman of the meeting shall   exercise his casting vote. </p>
              <p><strong>Art. 35.</strong> The   agenda at the annual general meeting shall contain the followings:</p>
              <p class="L35">(1)   Recognization of the previous annual general meeting report; </p>
              <p class="L35">(2) Annual   Report of the affairs of the Association; </p>
              <p class="L35">(3)   Consideration for approval of the balance sheet;</p>
              <p class="L35">(4)   Election of new directors of the board;</p>
              <p class="L35">(5)   Election of an auditor; </p>
              <p class="L35">(6) Other   matters, about which all members have been informed in advance as required by   Articles. </p>
