              <p>&nbsp;</p>
              <p align="left"><strong>Art.   16.</strong> The Board of Directors of the Association, consisting of not less than 7 but not more than 15 executive committee members, elected from the ordinary members at the general meeting, shall have charge of the administration of the Association's affairs.</p>
              <p><strong>Art. 17.</strong> Board shall be empowered to issue regulations for the members to comply with so   that objectives of the Association can be fulfilled. </p>
              <p><strong>Art. 18.</strong> The   Association's board of Directors may form sub-committee group, or groups as it   deems suitable, and may grant power and assign duties to the same to undertake   any particular works under the supervision of the Board. The sub-committee shall   prepare report of result of such particular work to the board and shall follow   the Board's instructions.</p>
              <p><strong>Art. 19.</strong> The   Board shall be empowered to invite person, or persons, outside the Association   to act as patron, consultant giving aids and advice to the Association, but such   person, or persons, shall have no right whatever in the administration of the   Association. </p>
              <p><strong>Art. 20. </strong>The   Board shall be empowered to appoint officials to administer the Association's   internal affairs as it deems suitable, and shall be empowered to determine   remuneration for such service. </p>
              <p><strong>Art. 21. </strong>The   directors of the Board shall be elected by resolution passed by simple majority   of the votes by ordinary members. </p>
              <p><strong>Art. 22.</strong> The Board who has been elected under Art. 16. shall hold election within themselves to fill the posts of</p>
              <p class="L35">(1)   president</p>
              <p class="L35">(2) first   vice-president</p>
              <p class="L35">(3) second   vice-president</p>
              <p class="L35">(4) third   vice-president</p>
              <p class="L35">(5) fourth vice-president</p>
              <p class="L35">(6)   treasurer </p>
              <p class="L35">(7)   committee-head of subcommittee for section of quality, research and   development</p>
              <p class="L35">(8)   committee-head of subcommittee for section of transportation and insurance </p>
              <p class="L35">(9)   committee-head of subcommittee for section of industry</p>
              <p class="L35">(10)   committee-head of subcommittee for section of agriculture </p>
              <p class="L35">(11)   committee-head of subcommittee for section of contract, price and marketting and   other posts as deemed suitable. A director shall hold office for a term of 2   years, and shall be eligible for re-election. </p>
              <p class="L35">President   shall have no term to hold office. </p>
              <p><strong>Art. 23.</strong> In   the case where the office of any director becomes vacant before the expiration   of his, or her, term of office, the remaining directors shall appoint any   ordinary members to take his, or her, place until the completion of the   remaining period of his, or her, term. </p>
              <p><strong>Art. 24.</strong> Besides the expiration of a director's term of office, he shall retire upon: </p>
              <p class="L35">(1) death; </p>
              <p class="L35">(2)   resignation; </p>
              <p class="L35">(3)   disqualification of membership under Art. 9;</p>
              <p class="L35">(4)   disqualification of membership under Art. 16; </p>
              <p class="L35">(5) removal   by general meeting's resolution passed by at least two-thirds majority of the   votes of members present at the meeting;</p>
              <p class="L35">(6) removal   by the order of the Minister of Commerce under the Trade Association Act, B.E.   2509; </p>
              <p class="L35">(7) absence   from 3 consecutive committee meeting will end the post of a committee member and   the President shall appoint a new committee member;</p>
              <p class="L35">(8) be   disable person and unable to work at all; </p>
              <p class="L35">(9) being   sentenced by final judgment of the court to become insolvent; </p>
              <p class="L35">(10) to be   sentenced to imprisonment by final judgment of the court, except a minor offense   or careless. </p>
              <p><strong>Art. 25. </strong>A   meeting of the Board shall be held at least once a month, or when the President,   the Vice-President or two other directors deem it suitable to instruct the   Secretary General to invoke special meeting. </p>
              <p><strong>Art. 26. </strong>A quorum for any meeting of the Board shall consist of not less than half of executive committee members and related representative(s) can be authorized or appointed to attend the meeting.</p>
              <p><strong>Art. 27. </strong>Upon receipt of the order under Art. 25, the Secretary General shall call   the meeting within 4 days counting from the day the order has been received, and   shall give the meeting agenda and the name of the person, or persons, calling   that meeting</p>
              <p><strong>Art. 28. </strong>Unless otherwise specified, the simple majority voting shall be followed in   all meetings of the Board. In the event of a tie, the President shall exercise   his cast vote. </p>
