              <p>&nbsp;</p>
              <p align="left"><strong>Art.   7.</strong> There shall be three categories of membership, namely: </p>
              <p class="L35">(1)   Ordinary members being natural or juristic persons engaged in natural rubber   trade and registered as required by laws; </p>
              <p class="L35">(2) Extra   ordinary members being natural or juristic persons engaged in natural rubber   trade, but have no right to vote on Executive Board election. </p>
              <p class="L35">(3)   Honorary members being distinguished persons who are interested in the rubber   trade or rubber industry, or being experts in the rubber affairs whom the Board   deems suitable to be invited to become Honorary members.</p>
              <p><strong>Art. 8. </strong>Applicant for ordinary and extra ordinary membership shall submit the   application form to the Association Secretary General, having been proposed by   one ordinary member and seconded by another. The Secretary General shall submit   the application to the Board at the first meeting after acceptance of that   application. Admission shall be in accordance with the resolution of the Board   passed by secret ballot, by not less than two thirds majority of the votes of   members present at the meeting. </p>
              <p><strong>Art. 9.</strong> As   the members of the Thai Rubber Association:- </p>
              <p class="L35">(1)   Ordinary member </p>
              <p>The Registration   fee payable to the Association by ordinary member is Baht 20,000 which shall be   paid within 30 days from the day the notification of admission is made by the   Secretary General, and together with the Registration fee, the ordinary member   shall pay the First Annual Subscription fee of Baht 30,000 to the Association   for the year in which he or she is admitted as member. Membership shall commence   when the payment of registration fee and first annual subscription fee have been   made.</p>
              <p class="L35">(2) Extra   Ordinary members </p>
              <p>The Registration   fee payable to the Association by ordinary member is Baht 15,000 which shall be   paid within 30 days from the day the notification of admission is made by the   Secretary General, and together with the Registration fee, the ordinary member   shall pay the First Annual Subscription fee of Baht 25,000 to the Association   for the year in which he or she is admitted as member. Membership shall commence   when the payment of registration fee and first annual subscription fee have been   made. </p>
              <p class="L35">(3)   Honorary members </p>
              <p>Neither   registration fee nor annual subscription is payable. </p>
              <p class="L35">Except the   above mentioned case, ordinary member shall, within january 31 of each year make   the payment for annual subscription fee to the Association the sum of Baht   20,000. |f the payment of Annual Subscription is not made by March 31 of each   year, the membership shall immediately cease. </p>
              <p class="L35">Besides the   Annual Subscription Fees as stated above, an ordinary member is to pay a special   fee according to his export volumn in each month as determined by the committee   but not more than 3 Baht per ton. In case of extra ordinary members is to pay   not more than 80 percent of ordinary members rate within the 15th day of the   following month. This income will be used for activities to serve the objectives   and to to promote further development of the association and its members. </p>
              <p><strong>Art. 10.</strong> Membership shall terminate upon: </p>
              <p class="L35">(1)   termination of engagement in natural rubber trade, in the case of juristic   person; </p>
              <p class="L35">(2)   resignation by submitting resignation form to the Board after having paid the   Annual Subscription fee and all other outstanding debts to the Association; </p>
              <p class="L35">(3) removal   from membership, which may be effected by any one of the following: </p>
              <p class="L65">A.   violation of rules, regulations of the Association; </p>
              <p class="L65">B.   bankruptcy, being insolvent, or being liquidated; </p>
              <p class="L65">C.   committing actions which may causse dishonour to the Rubber Trade; </p>
              <p class="L65">D.   failing to pay the annual subscription within 31 March and having no reasonable   reply accepted by Board of Directors ;</p>
              <p class="L65">E.   being removed on account of activities detrimental to the good name of the   Association by resolution of the Board by vote of not less than twothirds of the   directors present at the meeting.</p>
              <p><strong>Art. 11. </strong>As   the Prior to passing the penalty resolution under (10)(3)(E) the Secretary   General shall send a written notice to the member's place of business notifying   him of the charges raised against him together with the date, time and place of   the meeting at least 7 days before such meeting. The accused member shall have   the right to attend the meeting and to make statement in the meeting. The minute   of the meeting shall be fully recorded in the Association Meeting Report. </p>
              <p><strong>Art. 12.</strong> All   ordinary members shall have equal rights to attend meetings, to cast vote in the   general meeting, and to be elected directors of the Board.</p>
              <p><strong>Art. 13.</strong> All   extra ordinary and Honorary members shall have the right to attend general   meeting but shall neither have the right to vote nor to be elected directors of   the Board.</p>
              <p><strong>Art. 14.</strong> Members shall have the following duties: </p>
              <p class="L35">(1) to   adhere strictly to the rules and regulations of the Association; </p>
              <p class="L35">(2) to   maintain and cherish the honour and good name of the Association; </p>
              <p class="L35">(3) to   promote by all means the Association's activities for the progress and   prosperity; </p>
              <p class="L35">(4) to pay   all subscriptions and other debts. </p>
              <p><strong>Art. 15. </strong>All   members of both categories shall have the rights to look into the activities and   properties of the Association by submitting an application to that effect to the   Board. </p>
