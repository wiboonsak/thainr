                  <p>The rubber production of Thailand increased not only by strong demand from the world market, but was also stimulated strongly through the replanting program of the Thai Government from 1961 onwards. This resulted indirectly in the establishment of rubber processing factories and rubber trading companies. As these professional groups needed to be in close contact with the farmers and plantation owners, their activities spread throughout the rubber growing area in the South.</p>
                  <p>The number of members of the Thai Rubber Traders' Association has, therefore, increased and presently comprises 60 leading companies. Some of these companies are rubber processors while others are traders. The Association is recognized by the Government and is respected by the public as the central organization in which the government and the private sector can get together.</p>
