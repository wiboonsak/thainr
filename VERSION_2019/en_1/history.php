              <ul type="disc">
                <li><strong>Technical &amp; Business Know-How</strong><br>
                The Association supports members in  deriving knowledge and experience in rubber processing technique and rubber  business through exchange of ideas, supply of information, arranging of study  visits, inviting members to attend meetings, holding seminar and organizing  training for both local and overseas attendees on appropriate occasions.              </li>
              </ul>
              <ul type="disc">
                <li><strong>Rubber       Marketing</strong> <strong>Promotion</strong><br>
                The association organizes business  dinner annually. It also arranges missions to rubber consuming countries and  arranges for its members to attend annual dinners of various rubber  organizations overseas to strengthen the rubber business relationship,  especially with members of ASEAN Rubber Business Council namely Malaysia,  Indonesia, Singapore, Vietnam and Cambodia.</li>
              </ul>
              <ul type="disc">
                <li><strong>Rubber       Business Disputes</strong><br>
                The Association acts as a mediator  in settling rubber trade disputes among members and those between members and  non-members at local and international level.                </li>
              </ul>
              <p align="left">&nbsp;&nbsp;&nbsp;&nbsp;<strong>&nbsp;&nbsp; </strong><strong>2. COOPERATION WITH GOVERNMENT AGENCIES, PRIVATE SECTOR AND ACADEMIC AND RESEARCH INSTITUTES </strong><br>
                <br>
              &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Representatives of the Association serve as one of committee members in various government agencies namely Committee on Natural Rubber Policy, Rubber Control Committee, Thai Arbitration Committee on Natural Rubber, Committee on Consideration of Agricultural Product Standard for Natural Rubber and Rubber Products under National Bureau of Agricultural Commodity and Food Standards, Committee on Natural Rubber and Rubber products under Thai Industrial Standards Institute (TISI) and Sub-Committee on Standard Establishment of Odor Intensity Control in Rubber Factories. </p>
              <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; In cooperation with private sector, the Association is a member of Thai Chamber of Commerce's Committee on Key Plants of Economic Importance and Committee on Rubber and Rubber Wood Cluster under the Federation of Thai Industries and Trade Environment Committee under Thai National Shipper&rsquo;s Council. </p>
              <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; On research front, we also serve as a member of Committee on Southern Region Research Network under National Research Council of Thailand (NRCT), Expert Group of National Science and Technology Development Agency (NSTDA), Thailand Research Fund and related educational institutions.<br>
