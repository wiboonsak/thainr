              <dd>
                <p>On March 5, 1951, the Thai Rubber Association was established by those early traders under the name of <strong>&quot;The Rubber Traders Association of Thailand&quot;</strong>. Its objectives were to serve as the central organization of companies comprising rubber producers and traders to foster co-operation between them as well as acting as the central entity for government agencies to contact the group and discuss or negotiate about matters of common interest. From that humble beginning, the Association has also been the driving force in pushing innovative activities concerning career development along with up-grading its members' standards of business administration.</p>
                <p>The main purpose of the establishment of the Association at the beginning was to solve problems due to the non-regulated trade that lacked any rules and regulation. Buyers and sellers were free to trade and thus could satisfy their own needs and wishes without considering the impact on the trading as a whole. Understandably, there were problems of poor quality goods as well as difficulties of contractors who did not follow or abide with contracts. This occasionally resulted in lengthy disputes and harmed the trade and its traders.</p>
                <p>Consequently, a gathering of rubber traders calling themselves<strong> &quot;The Rubber Traders Association of Thailand&quot;</strong> was formed with the prime objectives to eliminate unfair trading and to support its' honest trading members in accordance with formulated rules and regulation.<br>
                  The office was appropriately located at the Bangkok Chamber of Commerce. The first meeting of the association was conducted on March 5, 1951. In its formative years, there were already 15 members including both ordinary members and special members.</p>
                <p>It took the Association almost two decades to become a regular member of the Thai Chamber of Commerce. On the 29th of February 1968 the permit to become a trade association was given by the registrar of the Government Authority. The office was relocated at 57 Soi Rong Muang 5, Rong Muang Road, Pathumwan District, Bangkok under the name<strong> &quot;The Thai Rubber Traders' Association&quot;</strong>.</p>
