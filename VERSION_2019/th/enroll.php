              <p><strong>ข้อ 30. </strong>ให้มีการประชุมใหญ่สามัญประจำปีภายใน 120 วัน นับแต่วันสิ้นปีการบัญชี ตามข้อ 2   หมวด 9 โดยเลขาธิการเป็นผู้บอกกล่าวให้สมาชิกทราบถึงกำหนดวัน เวลา สถานที่   และกิจการที่ประชุมล่วงหน้าอย่างน้อยเจ็ดวัน </p>
              <p><strong>ข้อ 31. </strong>คณะกรรมการจะเรียกประชุมใหญ่วิสามัญเมื่อใดก็ได้ โดยแจ้งให้สมาชิกทราบถึงวัน   เวลา สถานที่ และกิจการที่จะประชุมล่วงหน้าอย่างน้อยเจ็ดวัน เว้นแต่กรณีฉุกเฉิน   ซึ่งนายก หรืออุปนายกอาจเรียกประชุมใหญ่วิสามัญได้ โดยส่งหนังสือบอกกล่าวล่วงหน้า   หนึ่งวันทำการ ให้ถือเป็นหลักฐานเพียงพอ</p>
              <p><strong>ข้อ 32.</strong> สมาชิกสามัญจำนวนไม่ต่ำกว่าสองในห้าของจำนวนสมาชิกสามัญทั้งหมด   มีสิทธิเข้าชื่อกันทำเป็นหนังสือยื่นต่อคณะกรรมการให้เรียกประชุมใหญ่วิสามัญได้ โดยต้องระบุความมุ่งประสงค์ที่จะประชุม   เมื่อคณะกรรมการได้รับหนังสือแล้ว ให้เลขาธิการมีหนังสือบอกกล่าว   นัดประชุมใหญ่วิสามัญ โดยระบุวัน เวลา สถานที่ประชุม   และกิจการที่จะประชุมล่วงหน้าอย่างน้อยเจ็ดวัน <BR>
              </p>
              <p><strong>ข้อ 33.</strong> ให้นายกเป็นประธานที่ประชุมใหญ่ ในกรณีที่นายกไม่อยู่ในที่ประชุม   หรือไม่อาจมาประชุมได้ ให้อุปนายกเป็นประธานในที่ประชุม   ถ้าอุปนายกไม่อยู่ในที่ประชุม หรือไม่อาจมาประชุมได้ ให้สมาชิกสามัญ   หรือผู้แทนซึ่งมาประชุมเลือกสมาชิกสามัญ   หรือผู้แทนคนหนึ่งเป็นประธานในที่ประชุมเฉพาะการประชุมคราวนั้น</p>
              <p><strong>ข้อ 34.</strong> ในการประชุมใหญ่จะต้องมีสมาชิกสามัญ หรือผู้แทนมาเข้าประชุม   ไม่น้อยกว่าหนึ่งในสามของจำนวนสมาชิกสามัญทั้งหมด จึงจะเป็นองค์ประชุม</p>
              <p>มติของที่ประชุมให้ถือเสียงข้างมาก   ถ้าคะแนนเสียงเท่ากัน   ให้ประธานในที่ประชุมออกเสียงเพิ่มขึ้นอีกเสียงหนึ่งเป็นเสียงชี้ขาด<BR>
              </p>
              <p><strong>ข้อ 35. </strong>กิจการอันพึงกระทำในการประชุมใหญ่สามัญประจำปี   มีดังนี้</p>
              <blockquote>
                <p>(1)   รับรองรายงานการประชุมใหญ่คราวก่อน<br>
                  (2) รับทราบรายงานประจำปี   แสดงผลการดำเนินงานของสมาคมในรอบปีที่ผ่านมา<br>
                  (3)   พิจารณาอนุมัติงบดุล<br>
                  (4) เลือกตั้งกรรมการ   (ในปีที่ครบวาระ)<br>
