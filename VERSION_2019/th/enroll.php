              <p><strong>ข้อ 12.</strong> สมาชิกสามัญผู้ไม่ค้างค่าบำรุงรายปีและไม่ค้างค่าบำรุงพิเศษเกินกว่า   3 เดือนนับจากวันที่ได้รับแจ้งเป็นลายลักษณ์อักษรมีสิทธิเข้าประชุมใหญ่   และมีสิทธิออกเสียงในการเลือกตั้งคณะกรรมการ</p>
