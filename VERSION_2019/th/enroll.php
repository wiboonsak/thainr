                  ผู้สมัครเป็นสมาชิกสมทบจะได้สิทธิแห่งสมาชิกภาพเมื่อได้ชำระค่าธรรมเนียมแรกเข้าและค่าบำรุงรายปีแล้ว</p>
                <p><em>(4)  สมาชิกกิตติมศักดิ์</em>  ไม่ต้องชำระค่าธรรมเนียมแรกเข้าและค่าบำรุงรายปี<BR>
                  นอกจากค่าธรรมเนียมแรกเข้าและค่าบำรุงรายปีแล้วสมาชิกจะต้องชำระค่าบำรุงพิเศษ        ซึ่งคำนวณตามปริมาณยางพาราที่สมาชิกส่งออกไปจำหน่ายในต่างประเทศในแต่ละเดือน        ในอัตราที่คณะกรรมการกำหนด แต่ไม่เกินตันละสามบาท   สำหรับสมาชิกสามัญ,วิสามัญและสมทบ      ทั้งนี้จะต้องชำระภายในวันที่ 15 วัน   หลังจากที่ได้รับแจ้ง<BR>
                </p>
              </blockquote>
